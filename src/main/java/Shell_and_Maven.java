        // rm file_name removes file
        // touch file_name creates file
        // mv file_name folder_name moves file to folder
        // rm -rf folder_name loses everything from inside folder until it reaches folder and deletes it
        // recursive force = rf
        // ps shows running processes
        // vi file_name goes inside file and lets us edit it
        // i = insert mode, allows you to write
        // u removes last thing you wrote
        // x lets you go out of insert mode
        // :wq write and quit, lets you save
        // :q! quit without saving changes
        // cat file_name displays content of file
        // shell scripts end with .sh
        // sh shell_file_name runs shell script
        // whoami shows current user


